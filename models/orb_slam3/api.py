# Default Mapillary intrinsics (verified against EXIF, 2048×1536)
DEFAULT_FX = DEFAULT_FY = 1090.7
DEFAULT_CX = 1024.0
DEFAULT_CY = 768.0
DEFAULT_W  = 2048
DEFAULT_H  = 1536

_TS_PATTERN = re.compile(r'^(\d+)_')


def resolve_sequence(
    project_root: str,
    sequence: str = "s2",
) -> tuple[str, str, str]:
    """
    Resolve sequence directory, image directory, and output tag.

    Args:
        project_root: Drive root (e.g. /content/drive/MyDrive/P1)
        sequence:     one of "s1_segment", "s1_full", "s2"

    Returns:
        (seq_dir, img_dir, out_tag)
    """
    if sequence not in SEQUENCES:
        raise ValueError(f"Unknown sequence '{sequence}'. Choose from: {list(SEQUENCES)}")

    cfg     = SEQUENCES[sequence]
    seq_dir = os.path.join(project_root, "data", cfg["subdir"])
    img_dir = os.path.join(seq_dir, "images")
    out_tag = f"orb_{sequence}"
    print(f"Sequence: {sequence}  ({cfg['description']})")
    print(f"  Images : {img_dir}")
    print(f"  Out tag: {out_tag}")
    return seq_dir, img_dir, out_tag


# ── rgb.txt generation ────────────────────────────────────────────────────────

def build_rgb_txt(
    img_dir: str,
    seq_dir: str,
    filename: str = "rgb.txt",
    extensions: set[str] | None = None,
    overwrite: bool = False,
) -> str:
    """
    Generate rgb.txt (TUM-compatible) from {timestamp_ms}_{image_id}.jpg files.

    Timestamps are derived from the filename prefix.
    Safe to re-run — skips creation if file already exists (unless overwrite=True).

    Args:
        img_dir:    directory containing images
        seq_dir:    sequence root (rgb.txt written here)
        filename:   output filename
        extensions: allowed file extensions (default: jpg/jpeg/png)
        overwrite:  regenerate even if file exists

    Returns:
        Absolute path to rgb.txt
    """
    if extensions is None:
        extensions = {".jpg", ".jpeg", ".png"}

    out_path = os.path.join(seq_dir, filename)
    if os.path.exists(out_path) and not overwrite:
        print(f"rgb.txt already exists: {out_path}  (pass overwrite=True to regenerate)")
        return out_path

    entries = []
    for fname in os.listdir(img_dir):
        if Path(fname).suffix.lower() not in extensions:
            continue
        m = _TS_PATTERN.match(fname)
        if m:
            entries.append((int(m.group(1)), fname))
    entries.sort(key=lambda x: x[0])

    if not entries:
        raise FileNotFoundError(f"No images found in {img_dir}")

    with open(out_path, "w") as f:
        f.write("# TUM RGB-D format\n")
        f.write("# timestamp filename\n")
        f.write("# generated by models.orb_slam3.api\n")
        for ts_ms, fname in entries:
            rel = os.path.join("images", fname)
            f.write(f"{ts_ms / 1000.0:.6f} {rel}\n")

    print(f"rgb.txt written: {out_path}  ({len(entries)} images)")
    return out_path


# ── Settings YAML generation ──────────────────────────────────────────────────

def write_settings_yaml(
    seq_dir: str,
    fx: float = DEFAULT_FX,
    fy: float = DEFAULT_FY,
    cx: float = DEFAULT_CX,
    cy: float = DEFAULT_CY,
    width: int = DEFAULT_W,
    height: int = DEFAULT_H,
    fps: float = 30.0,
    orb_n_features: int = 1000,
    orb_scale_factor: float = 1.2,
    orb_n_levels: int = 8,
    filename: str = "orb_slam3_settings.yaml",
    overwrite: bool = False,
) -> str:
    """
    Write an ORB-SLAM3 monocular settings YAML.

    Derived from EXIF data for standard Mapillary sequences.
    Safe to re-run — skips if file exists (unless overwrite=True).

    Args:
        seq_dir:          sequence root directory
        fx, fy, cx, cy:  camera intrinsics
        width, height:   image resolution
        fps:             target frame rate
        orb_n_features:  ORB feature count
        orb_scale_factor: ORB scale factor per level
        orb_n_levels:    ORB pyramid levels
        filename:        output filename
        overwrite:       regenerate even if file exists

    Returns:
        Absolute path to the settings YAML
    """
    out_path = os.path.join(seq_dir, filename)
    if os.path.exists(out_path) and not overwrite:
        print(f"Settings YAML already exists: {out_path}  (pass overwrite=True to regenerate)")
        return out_path

    content = f"""%YAML:1.0

# ORB-SLAM3 Monocular settings
# Generated by models.orb_slam3.api  — do not edit manually

Camera.type: "PinHole"

Camera.fx: {fx:.4f}
Camera.fy: {fy:.4f}
Camera.cx: {cx:.4f}
Camera.cy: {cy:.4f}

Camera.k1: 0.0
Camera.k2: 0.0
Camera.p1: 0.0
Camera.p2: 0.0

Camera.width:  {width}
Camera.height: {height}

Camera.fps: {fps:.1f}

Camera.RGB: 1

ORBextractor.nFeatures:    {orb_n_features}
ORBextractor.scaleFactor:  {orb_scale_factor:.4f}
ORBextractor.nLevels:      {orb_n_levels}
ORBextractor.iniThFAST:    20
ORBextractor.minThFAST:    7

Viewer.KeyFrameSize:      0.05
Viewer.KeyFrameLineWidth: 1.0
Viewer.GraphLineWidth:    0.9
Viewer.PointSize:         2.0
Viewer.CameraSize:        0.08
Viewer.CameraLineWidth:   3.0
Viewer.ViewpointX:        0.0
Viewer.ViewpointY:       -0.7
Viewer.ViewpointZ:       -1.8
Viewer.ViewpointF:        500.0
"""

    with open(out_path, "w") as f:
        f.write(content)
    print(f"Settings YAML written: {out_path}")
    return out_path


# ── Active image list ─────────────────────────────────────────────────────────

def build_active_list(
    rgb_txt_path: str,
    seq_dir: str,
    skip: int = 4,
    frame_start: int = 0,
    frame_end: int = -1,
) -> tuple[list[str], list[float]]:
    """
    Build the active image list from rgb.txt, applying FRAME_START/END and SKIP.

    Args:
        rgb_txt_path: path to rgb.txt
        seq_dir:      sequence root (image paths are relative to this)
        skip:         frame stride
        frame_start:  first frame index
        frame_end:    last frame index (-1 = all)

    Returns:
        image_paths : absolute paths to selected images
        timestamps  : corresponding timestamps in seconds
    """
    with open(rgb_txt_path) as f:
        lines = [l for l in f if l.strip() and not l.startswith("#")]

    if frame_end == -1:
        frame_end = len(lines)
    lines = lines[frame_start:frame_end:skip]

    image_paths = []
    timestamps  = []
    for line in lines:
        parts = line.strip().split()
        ts, rel = float(parts[0]), parts[1]
        abs_path = os.path.join(seq_dir, rel)
        image_paths.append(abs_path)
        timestamps.append(ts)

    print(f"Active list: {len(image_paths)} frames (skip={skip}, [{frame_start}:{frame_end}])")
    return image_paths, timestamps


# ── KeyFrameTrajectory loading ────────────────────────────────────────────────

def load_keyframe_trajectory(path: str) -> np.ndarray:
    """
    Load ORB-SLAM3 KeyFrameTrajectory.txt in TUM format.

    Format: timestamp tx ty tz qx qy qz qw

    Args:
        path: path to KeyFrameTrajectory.txt

    Returns:
        (N, 8) float64 array
    """
    traj = np.loadtxt(path)
    if traj.ndim == 1:
        traj = traj.reshape(1, -1)
    assert traj.shape[1] == 8, f"Expected 8 TUM columns, got {traj.shape[1]}"
    print(f"KeyFrameTrajectory: {len(traj)} keyframes from {path}")
    return traj


# ── Parallax / displacement diagnostic ───────────────────────────────────────

def displacement_diagnostic(
    image_paths: list[str],
    n_pairs: int = 10,
    min_px: float = 25.0,
    max_px: float = 150.0,
) -> dict:
    """
    Compute mean inter-frame optical flow displacement for the first n_pairs pairs.

    ORB-SLAM3 initialises best in [25, 150] px range.
    Below 25 px → too little parallax; above 150 px → insufficient match overlap.
    Adjust SKIP until the mean falls in the valid range.

    Args:
        image_paths: ordered list of image paths (already skip-filtered)
        n_pairs:     number of consecutive frame pairs to measure
        min_px:      minimum valid mean displacement (px)
        max_px:      maximum valid mean displacement (px)

    Returns:
        dict with keys: mean_px, status, pairs measured
    """
    try:
        import cv2
    except ImportError:
        raise ImportError("pip install opencv-python")

    disps = []
    for i in range(min(n_pairs, len(image_paths) - 1)):
        prev = cv2.imread(image_paths[i],     cv2.IMREAD_GRAYSCALE)
        curr = cv2.imread(image_paths[i + 1], cv2.IMREAD_GRAYSCALE)
        if prev is None or curr is None:
            continue
        flow = cv2.calcOpticalFlowFarneback(prev, curr, None, 0.5, 3, 15, 3, 5, 1.2, 0)
        disps.append(float(np.sqrt(flow[..., 0] ** 2 + flow[..., 1] ** 2).mean()))

    if not disps:
        return {"mean_px": 0.0, "status": "no_images", "n_pairs": 0}

    mean_px = float(np.mean(disps))
    if mean_px < min_px:
        status = "too_low — increase SKIP or FRAME_START"
    elif mean_px > max_px:
        status = "too_high — decrease SKIP"
    else:
        status = "ok"

    print(f"Displacement diagnostic: {mean_px:.1f} px over {len(disps)} pairs → {status}")
    return {"mean_px": mean_px, "status": status, "n_pairs": len(disps)}
